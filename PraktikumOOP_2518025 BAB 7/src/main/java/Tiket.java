public class Tiket {

    protected int harga;
    protected int jumlah;

    public void setHarga(int harga) {
        this.harga = harga;
    }

    public void setJumlah(int jumlah) {
        this.jumlah = jumlah;
    }

    public int total() {
        return harga * jumlah;
    }
}